import {
  CREATE_STATISTIK_SUCCESS,
  CREATE_STATISTIK_FAIL,
  RETRIEVE_STATISTIK_SUCCESS,
  RETRIEVE_STATISTIK_FAIL,
  UPDATE_STATISTIK_SUCCESS,
  UPDATE_STATISTIK_FAIL,
  DELETE_STATISTIK_SUCCESS,
  DELETE_STATISTIK_FAIL,
  SET_MESSAGE,
} from "./types";

import Service from "src/services/statistik.service";
import EventBus from "src/utils/EventBus";

export const retrieve = () => async (dispatch) => {
  try {
    const res = await Service.getAll();

    dispatch({
      type: RETRIEVE_STATISTIK_SUCCESS,
      payload: res.data,
    });
  } catch (err) {
    if (err.response && err.response.status === 401) {
      EventBus.dispatch("logout");
    }
    console.log(err);
  }
};

